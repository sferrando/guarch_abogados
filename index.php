$("#formulario").submit(function(e) {
  var url = "mail.php";

  $.ajax({
          type: "POST",
          url: url,
          data: $("#formulario").serialize(),
          success: function(data)
          {
              $("#before_send_email").hide();
              $("#meanwhile_send_email").show();
              setTimeout(function () {
                $("#meanwhile_send_email").hide();
                $("#after_send_email").show();
              }, 3000);
          }
        });

  e.preventDefault();
});

